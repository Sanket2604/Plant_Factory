function removeanimation(){
    document.querySelector(".gmail").style.animationName = 'none';
    document.querySelector(".fac").style.animationName = 'none';
    document.querySelector(".twit").style.animationName = 'none';
    document.querySelector(".linked").style.animationName = 'none';
    document.querySelector(".inst").style.animationName = 'none';
}

function addanimation(){
    document.querySelector(".gmail").style.animationName = 'gmail';
    document.querySelector(".fac").style.animationName = 'fac';
    document.querySelector(".twit").style.animationName = 'twit';
    document.querySelector(".linked").style.animationName = 'linkedin';
    document.querySelector(".inst").style.animationName = 'insta';
}
